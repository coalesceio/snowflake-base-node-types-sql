WITH SALES_CTE AS (
 
    SELECT EMP_ID, JAN_SALES, FEB_SALES, MAR_SALES
    FROM VALUES
        (101, 1000, 1200, 1500),
        (102,  900, 1100, 1400)
    AS T(EMP_ID, JAN_SALES, FEB_SALES, MAR_SALES)
 
),
 
UNPIVOT_CTE AS (
    SELECT
        EMP_ID,
        MONTH_NAME,
        SALES_AMOUNT
    FROM SALES_CTE
    UNPIVOT (
        SALES_AMOUNT FOR MONTH_NAME IN (
            JAN_SALES,
            FEB_SALES,
            MAR_SALES
        )
    )
)
 
SELECT EMP_ID, MONTH_NAME, SALES_AMOUNT
FROM UNPIVOT_CTE
ORDER BY EMP_ID, MONTH_NAME